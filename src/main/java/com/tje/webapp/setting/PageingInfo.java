package com.tje.webapp.setting;


//servlet context에도 등록
public class PageingInfo {
	
	//웹 페이지에서 페지이를 나누기
	private int pageingSize;
	//
	private int pageRange;
	
	public PageingInfo() {
	}

	public int getPageingSize() {
		return pageingSize;
	}

	public void setPageingSize(int pageingSize) {
		this.pageingSize = pageingSize;
	}

	public int getPageRange() {
		return pageRange;
	}

	public void setPageRange(int pageRange) {
		this.pageRange = pageRange;
	}

	
	
	
	
	
	
	
}





















